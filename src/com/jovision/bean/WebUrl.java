package com.jovision.bean;

public class WebUrl {
	private String demoUrl;
	private String custUrl;
	private String statUrl;
	private String bbsUrlString;

	public String getDemoUrl() {
		return demoUrl;
	}

	public void setDemoUrl(String demoUrl) {
		this.demoUrl = demoUrl;
	}

	public String getCustUrl() {
		return custUrl;
	}

	public void setCustUrl(String custUrl) {
		this.custUrl = custUrl;
	}

	public String getStatUrl() {
		return statUrl;
	}

	public void setStatUrl(String statUrl) {
		this.statUrl = statUrl;
	}

	public String getBbsUrlString() {
		return bbsUrlString;
	}

	public void setBbsUrlString(String bbsUrlString) {
		this.bbsUrlString = bbsUrlString;
	}

}
